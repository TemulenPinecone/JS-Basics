function day(x) {
  if (x == 1) {
    console.log("Monday");
  } else if (x == 2) {
    console.log("Tuesday");
  } else if (x == 3) {
    console.log("Wednesday");
  } else if (x == 4) {
    console.log("Thursday");
  } else if (x == 5) {
    console.log("Friday");
  } else if (x == 6) {
    console.log("Saturday");
  } else if (x == 7) {
    console.log("Sunday");
  } else {
    console.log("Error!");
  }
}

day(1);
day(2);
day(3);
day(4);
day(5);
day(6);
day(7);

day(8);
day(0);
day(0.1);
