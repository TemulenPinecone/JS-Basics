function numToString(x) {
  if (x == 2) {
    console.log("Muu");
  } else if (x == 3) {
    console.log("Dund");
  } else if (x == 4) {
    console.log("Sain");
  } else if (x == 5) {
    console.log("Onts");
  } else {
    console.log("Error");
  }
}

numToString(2);
numToString(3);
numToString(4);
numToString(5);
numToString(6);
